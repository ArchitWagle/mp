DATA SEGMENT
MSG1 DB 10,13,"ENTER 1ST NO: $"

MSG2 DB 10,13,"ENTER 2ND NO: $"

MSG3 DB 10,13,"RESULT: $"

DATA ENDS

CODE SEGMENT
ASSUME DS:DATA,CS:CODE
START:
MOV AX,DATA
MOV DS,AX

LEA DX,MSG1
MOV AH,09H
INT 21H

MOV AH,01H
INT 21H
CALL INPUT

ROL AL,04H
MOV BL,AL

MOV AH,01H
INT 21H
CALL INPUT
ADD BL,AL

LEA DX,MSG2
MOV AH,09H
INT 21H

MOV AH,01H
INT 21H

CALL INPUT

ROL AL,04H
MOV CL,AL

MOV AH,01H
INT 21H

CALL INPUT
ADD CL,AL

ADD BL,CL
MOV CL,BL

LEA DX,MSG3
MOV AH,09H
INT 21H

AND BL,0F0H
ROR BL,04H
CALL OUTPUT

MOV DL,BL

MOV AH,02H
INT 21H

MOV BL,CL
AND BL,0FH
CALL OUTPUT
MOV DL,BL

MOV AH,02H
INT 21H

MOV AH,4CH
INT 21H

INPUT PROC
CMP AL,41H
JC L1
SUB AL,07H
L1: SUB AL,30H
RET
ENDP

OUTPUT PROC
CMP BL,0AH
JC L2
ADD BL,07H
L2: ADD BL,30H
RET
ENDP

CODE ENDS
END START
